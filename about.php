            <h2>Why I created MambaAndMe</h2>

            <p>
                Growing up, I loved watching the Lakers. The three-peat from 2000-2002 was amazing. I still remember seeing the Lakers play the Vancouver Grizzlies live in 2001. Admittedly, I cheered for the 
                Lakers, rather than my hometown Grizzles. I still look back on the pictures that I took of Kobe Bryant, Shaq, Mike Bibby, Rick Fox and Brian Shaw coming out of the locker room, tucked in my photo album.
                As the game ended, I remember high-fiving Shaq, whose hands were gigantic compared to my tiny child hand.
                Fast-forward a few years, and I realized I wasn't only a Lakers fan. I was a Kobe fan. His games, his highlights when they came on TV before school, his game winners, clutch shots, and the confidence he exuded with the
                ball in his hands, was admirable. It's what drove me to practice with our hoop in the backyard, rain or shine.
            </p>
            <br/>
            <p>
                Skipping to 2009-2010, he proved everyone wrong by leading the Lakers to two more championships. Those games brought me to the edge of my seat. In 2013, he tore his achilles and my heart sank. Kobe’s tenacity and drive brought the Lakers as far as they did, to the playoffs. Shooting two free throws and walking off the court with a torn achilles shows dedication to the craft. Moving to his final game as a player, I remember exactly where I was when I watched his final 60 point game. The game ran during a record-setting Golden State
                Warriors game, but the channel never changed.
            </p>
            <br/>
            <p>
               As a young person starting my 20's, I was anxious and unsettled about life's uncertainties. But then, I watched Kobe off the court.
               His speeches, motivational talks, and what the Mamba Mentality means. I listened to him constantly. I downloaded the recordings on my phone and iPod to listen at the gym, where his words charged me with motivation, self-worth, and the confidence to do
               anything I put my mind to. The death of Kobe and his daughter left me in shock and disbelief. I wanted to get these words out and wanted a platform dedicated to the profound impact that Kobe had on the lives of others through the Mamba Mentality. 
